#include <iostream>
using namespace std;

int main(){
    int num1, num2;
    cout << "Enter num1 : ";
    cin >> num1;
    cout << "Enter num2 : ";
    cin >> num2;
    
    cout << "Sum is = " << (num1 + num2) << endl;
    cout << "difference is = " << (num1 - num2) << endl;
    cout << "product is = " << (num1 * num2) << endl;
    cout << "quotient is = " << (num1 / num2) << endl;
    cout << "modulo is = " << (num1 % num2) << endl;
    
    return 0;
}
